#include "iostream"
using namespace std;

/*
 * 知识点总结和注意点：
 *
 * 1. 读取字符：
 *    - 使用 `cin.get(ch)` 来逐个读取字符，包括空格和换行符。
 *
 * 2. 字符处理：
 *    - 判断字符是否为换行符 `'\n'`，如果是则直接输出该字符。
 *    - 如果不是换行符，则将字符自增1后输出（即输出下一个字符的 ASCII 值）。
 *
 * 3. 循环结束条件：
 *    - 当遇到字符 `'.'` 时结束循环。
 *
 * 注意点：
 *
 * - 初始读取：
 *   - 在循环外先读取第一个字符，确保进入循环时 `ch` 已经初始化。
 *
 * - 循环条件：
 *   - 循环条件是 `ch != '.'`，确保遇到 `'.'` 时结束读取。
 *
 * - 自增输出：
 *   - 对非换行符字符进行自增操作并输出。
 */

int main()
{
    char ch;
    cin.get(ch); // 读取第一个字符
    while (ch != '.') // 判断是否为'.'，如果是则结束循环
    {
        if (ch == '\n') // 判断是否为换行符
            cout << ch; // 直接输出换行符
        else
            cout << ++ch; // 将字符自增后输出
        cin.get(ch); // 读取下一个字符
    }
    cout << endl;

    return 0;
}
