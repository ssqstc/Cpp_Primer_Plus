/*
知识点：
1. 使用 `cin.peek()` 查看输入流中的下一个字符，而不从输入流中移除它。
2. 使用 `cin.get()` 从输入流中读取单个字符。
3. 输入终止条件为 `$` 符号，统计输入字符的数量。

注意点：
1. `cin.peek()` 不会提取字符，仅查看，确保输入流中的 `$` 作为终止符不会被消耗。
2. 注意程序只能统计在终止符 `$` 之前的字符数量，输入 `$` 后循环终止。
*/

#include <iostream>   // 包含输入输出流库
using namespace std;  // 使用标准命名空间

int main() {
    char ch;          // 存储用户输入的字符
    int count = 0;    // 统计字符数量
    cout << "Enter a string: ";  // 提示用户输入字符串

    // 当输入的下一个字符不是 '$' 时继续循环
    while (cin.peek() != '$') {
        cin.get(ch);   // 获取并读取一个字符
        count++;       // 字符计数器加 1
    }

    // 输出字符总数（不包括终止符 '$'）
    cout << "count = " << count;

    return 0;  // 返回 0，表示程序成功执行
}