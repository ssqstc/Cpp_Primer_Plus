/*
 * 知识点总结：
 * - 构造函数重载：提供两个构造函数，允许不同方式初始化对象。
 * - 命名空间：使用 SALES 命名空间封装类和函数，避免命名冲突。
 * - 成员函数：提供函数显示销售数据和计算统计信息。
 *
 * 注意点：
 * - 数组边界：注意数组长度，确保不会越界。
 * - 输入处理：处理输入时，确保读取正确类型的数据，并进行必要的边界检查。
 * - 默认构造函数：提示用户输入四个季度的销售额，并计算最大值、最小值和平均值。
 */

/*
 * 主函数：演示 Sales 类的使用
 * - 创建 Sales 对象并显示其信息
 */
int main(){
    using namespace SALES;

    double ar[4] = {11.1, 22.2, 33.3, 44.4}; // 初始化销售数据
    Sales sa1(ar, 4); // 使用带参数的构造函数创建对象
    sa1.showSales(); // 显示对象 sa1 的销售数据

    Sales sa2; // 使用默认构造函数创建对象
    sa2.showSales(); // 显示对象 sa2 的销售数据
    return 0;
}
