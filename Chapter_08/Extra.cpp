#include "iostream"
using namespace std;

/*
 * 左值和右值：
 * 左值：具有内存地址，可以持久存在，可以赋值或取地址。
 * 右值：临时值，没有持久地址，不能直接赋值或取地址。
 *
 * 左值引用：绑定到左值，允许通过引用访问和修改。左值引用赋值运算符的右侧必须是左值。
 * 右值引用：绑定到右值，用于移动语义和完美转发。
 */

int main(){
    int a = 10;  // 定义变量 a
    int b = 20;  // 定义变量 b

    int &f = a;  // 左值引用 f 绑定到变量 a
//    int &d = 10;  // 错误：不能将左值引用绑定到字面量
//    int &d = (a + b);  // 错误：不能将左值引用绑定到表达式结果
    const int &d = 10;  // 常量左值引用，可以绑定到右值，因为编译器会创建临时变量
    const int &c = (a + b);  // 常量左值引用，可以绑定到右值表达式结果

    int &&x = 10;  // 右值引用 x 绑定到字面量 10
    int &&y = (a + b);  // 右值引用 y 绑定到表达式 (a + b) 的结果

    return 0;
}
